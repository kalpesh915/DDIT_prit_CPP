#include<iostream>

/**
    Incremenet operator / decrement operator
*/

using namespace std;

int main(){
    int ip1 = 10;
    cout<<ip1<<endl;
    ip1++;
    cout<<ip1<<endl;
    ip1++;
    cout<<ip1<<endl;
    ip1++;
    cout<<ip1<<endl;
    ip1--;
    cout<<ip1<<endl;
    ip1--;
    cout<<ip1<<endl;
    return 0;
}
